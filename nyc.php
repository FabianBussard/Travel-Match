            <h2>Sights to See</h2>
            <p> 
                <ul>
                    <li>
                        The Empire State Building is one of New York City's most famous attractions with
                        many flocking to the building to take an elevator ride to the 86th floor for a 
                        view from the observation deck.
                    </li>
                    <li>
                        Tourists from around the world flock to see the Statue of Liberty which was given
                        to the citizens of the United States in 1886 from the people of France. Visitors can hop
                        on a ferry and take the quick ride over to Ellis Island where they can make the 
                        climb up to the crown for stunning views.
                    </li>
                    <li>
                        The Metropolitan Museum of Art is about 13 acres in size and boasts over two 
                        million pieces from classical art to more modern pieces. This is a great place to
                        visit for those with free time during the week and in the morning.
                    </li>
                    <li>
                        Central Park is one of the world's most famous public parks spanning 843 acres.
                        This park offers a getaway from the hustle and bustle of city life and offers 
                        beautiful scenery along with several photo-worthy locations.
                    </li>
                    <li>
                        It is difficult to visit New York City without setting foot in Times Square. This
                        area is a must-see location for every tourist visiting the city. The area was once
                        known as a prime location for sex shops and drug deals, but has since turned its 
                        image around and is now a hub for shopping and visiting the pedestrian plaza along
                        Broadway.
                    </li>
                </ul>
